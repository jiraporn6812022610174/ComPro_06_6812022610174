#include <stdio.h>

int main() {
    int a = 5, b = 2;
    float x = 3.0, y = 4.5;

    // ประเมินค่า r1
    int r1 = a++ * b + (int)y % 3;
    // a++ = 5 (ใช้ก่อน แล้วค่อย +1) → a จะเป็น 6 หลังบรรทัดนี้
    // (int)y = 4, 4 % 3 = 1
    // r1 = 5*2 + 1 = 10 + 1 = 11
    printf("r1 = %d (a = %d หลังการเพิ่ม)\n", r1, a);

    // ประเมินค่า r2
    int r2 = (a > b) && ((int)x / b < 2);
    // a = 6, b = 2, x = 3.0
    // (a > b) = 6 > 2 = true (1)
    // ((int)x / b) = 3 / 2 = 1, 1 < 2 = true (1)
    // r2 = 1 && 1 = 1
    printf("r2 = %d\n", r2);

    // ประเมินค่า r3
    float r3 = ++x * y - a / 2;
    // ++x = 4.0, y = 4.5, a = 6
    // 4.0 * 4.5 = 18.0
    // 6 / 2 = 3
    // r3 = 18.0 - 3 = 15.00
    printf("r3 = %.2f (x = %.2f หลังเพิ่ม)\n", r3, x);

    // ประเมินค่า r4
    float r4 = ((x += 1.5) > y) || (b-- > 0);
    // x += 1.5 → x = 5.5
    // (5.5 > 4.5) = true → ไม่ประเมิน b-- เพราะ || short-circuit
    // r4 = true = 1.0
    printf("r4 = %.2f (x = %.2f, b = %d หลังประเมิน)\n", r4, x, b);

    return 0;
}