#include <stdio.h>

int main() {
    // -------------------------
    // W06-02-01: แบบ int
    // -------------------------
    int i = 1, j = 2, k;

    // 1
    printf("W06-02-01 (int)\n");
    printf("1: i = %d, j = %d, k = (ยังไม่กำหนด)\n", i, j);

    // 2
    k = i + j;
    printf("2: i = %d, j = %d, k = %d\n", i, j, k);

    // 3
    i = i + (k * j);
    printf("3: i = %d, j = %d, k = %d\n", i, j, k);

    // 4
    j = i / 2;
    printf("4: i = %d, j = %d, k = %d\n", i, j, k);

    // 5
    k = i % 2;
    printf("5: i = %d, j = %d, k = %d\n", i, j, k);

    // 6
    i = (j + k) * 3;
        printf("6: i = %d, j = %d, k = %d\n\n", i, j, k);


    // -------------------------
    // W06-02-02: แบบ double
    // -------------------------
    double x = 1.0, y = 2.0, z;

    // 1
    printf("W06-02-02 (double)\n");
    printf("1: x = %.1f, y = %.1f, z = (ยังไม่กำหนด)\n", x, y);

    // 2
    x = y + 5.0;
    printf("2: x = %.1f, y = %.1f, z = (ยังไม่กำหนด)\n", x, y);

    // 3
    y = x / 2.0;
    printf("3: x = %.1f, y = %.1f, z = (ยังไม่กำหนด)\n", x, y);

    // 4
    y = (x * 3.0) + 4.0;
    printf("4: x = %.1f, y = %.1f, z = (ยังไม่กำหนด)\n", x, y);

    // 5
    x = -0.5 - y;
    printf("5: x = %.1f, y = %.1f, z = (ยังไม่กำหนด)\n", x, y);

    // 6
    z = x + y;
    printf("6: x = %.1f, y = %.1f, z = %.1f\n", x, y, z);

    return 0;
}


/*
======== คำอธิบาย W06-02-01 (int) ========

1: กำหนด i = 1, j = 2, k ยังไม่กำหนดค่า
2: k = i + j = 1 + 2 = 3
3: i = i + (k * j) = 1 + (3 * 2) = 7
4: j = i / 2 = 7 / 2 = 3 (หารแบบ int ปัดเศษ)
5: k = i % 2 = 7 % 2 = 1
6: i = (j + k) * 3 = (3 + 1) * 3 = 12

======== คำอธิบาย W06-02-02 (double) ========

1: กำหนด x = 1.0, y = 2.0, z ยังไม่กำหนดค่า
2: x = y + 5.0 = 2.0 + 5.0 = 7.0
3: y = x / 2.0 = 7.0 / 2.0 = 3.5
4: y = (x * 3.0) + 4.0 = (7.0 * 3.0) + 4.0 = 25.0
5: x = -0.5 - y = -0.5 - 25.0 = -25.5
6: z = x + y = -25.5 + 25.0 = -0.5
*/