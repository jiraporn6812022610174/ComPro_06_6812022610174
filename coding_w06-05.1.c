#include <stdio.h>

int main() {
    int A, B, C, D, E, F, G;

    // คำนวณแต่ละนิพจน์
    A = -2 + 5 * 2; // A = -2 + 10 = 8
    B = 10 / 2 * 3; // B = 5 * 3 = 15
    C = 6 / 2 + 3 * (4 % 2); // C = 3 + 3 * 0 = 3
    D = (5 + 2) * 15 % 4; // D = 7 * 15 % 4 = 105 % 4 = 1
    E = 6 + 2 * 2 - 6 / 2; // E = 6 + 4 - 3 = 7
    F = 5 + 3 * 2 - 8 / 4 + (6 % 5); // F = 5 + 6 - 2 + 1 = 10
    G = (4 + 3) * 2 - 10 / (2 + 3); // G = 7 * 2 - 10 / 5 = 14 - 2 = 12

    // แสดงผลลัพธ์
    printf("A = %d\n", A);
    printf("B = %d\n", B);
    printf("C = %d\n", C);
    printf("D = %d\n", D);
    printf("E = %d\n", E);
    printf("F = %d\n", F);
    printf("G = %d\n", G);

    return 0;
}