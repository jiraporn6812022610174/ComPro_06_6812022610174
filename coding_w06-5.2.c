#include <stdio.h>

int main() {
// คำนวณและแสดงผลนิพจน์ต่าง ๆ ตามลำดับ

int A = -2 + 5 * 2; // 5*2=10, -2+10=8
printf("A = %d\n", A);

int B = 10 / 2 * 3; // 10/2=5, 5*3=15
printf("B = %d\n", B);

int C = 6 / 2 + 3 * (4 % 2); // 6/2=3, 4%2=0, 3*0=0, 3+0=3
printf("C = %d\n", C);

int D = (5 + 2) * 15 % 4; // (5+2)=7, 7*15=105, 105%4=1
printf("D = %d\n", D);

int E = 6 + 2 * 2 - 6 / 2; // 2*2=4, 6/2=3, 6+4-3=7
printf("E = %d\n", E);

int F = 5 + 3 * 2 - 8 / 4 + (6 % 5); // 3*2=6, 8/4=2, 6%5=1, 5+6-2+1=10
printf("F = %d\n", F);

int G = (4 + 3) * 2 - 10 / (2 + 3); // (4+3)=7, 7*2=14, (2+3)=5, 10/5=2, 14-2=12
printf("G = %d\n", G);

return 0;
}